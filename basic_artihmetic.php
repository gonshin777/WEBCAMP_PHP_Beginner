<?php

/* 四則演算 */
// 2つの変数を作る
$i = random_int(1,999);
$j = random_int(1,999);

// 加算
$k = $i + $j;
// 結果の出力
echo "{$i} + {$j} = {$k} \n";

// 減算
$k = $i - $j;
// 結果の出力
echo "{$i} - {$j} = {$k} \n";

// 乗算
$k = $i * $j;
// 結果の出力
echo "{$i} * {$j} = {$k} \n";

// 除算
$k = $i / $j;
// 結果の出力
echo "{$i} / {$j} = {$k} \n";

// 剰余
$k = $i % $j;
// 結果の出力
echo "{$i} % {$j} = {$k} \n";

// 冪乗1
$k = $i ** 3;
// 結果の出力
echo "${k} = $i ** 3 \n";

// 冪乗2
$k = pow($i,3);
// 結果の出力
echo "${k} = prw(${i},3) \n";